#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (!lst || !new)
		return ;
	new->next = *lst;
	*lst = new;
}
/*
lst: the address of the pointer to the first link of the list.
*lst: the pointer of the first link of a list.
*/